public class ActividadResuelta1p5 {

	public static void main(String[] args) {
		/* 
		 * Escribe un programa que compruebe que el rango de valores del tipo SHORT
		 * se comporte de forma ciclica. 
		 */ 
		
			
		short variable = 32767;
		
		System.out.println("El valor de la variable actual es " + variable);
			
		variable ++;
		
		System.out.println("El valor de la variable tras un incremento es " + variable);

	}

}
